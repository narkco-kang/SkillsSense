/**
 * ZIP Packager for Custom Skills
 *
 * Takes a generated skill and creates a downloadable ZIP package
 * with the standard skill file structure.
 */

import JSZip from "jszip";
import type { GeneratedSkill } from "./skill-generator";

export type PackagedSkill = {
  zip: Buffer;
  slug: string;
  files: string[];
};

function slugify(name: string): string {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
}

function getFileExtensions(skillName: string, tags: string[]): {
  codeExt: string;
  configExt: string;
} {
  const tagStr = tags.join(" ").toLowerCase();

  if (tagStr.includes("python")) return { codeExt: ".py", configExt: ".yml" };
  if (tagStr.includes("javascript") || tagStr.includes("typescript") || tagStr.includes("node"))
    return { codeExt: ".js", configExt: ".json" };
  if (tagStr.includes("rust")) return { codeExt: ".rs", configExt: ".toml" };
  if (tagStr.includes("go") || tagStr.includes("golang"))
    return { codeExt: ".go", configExt: ".yaml" };
  if (tagStr.includes("shell") || tagStr.includes("bash") || tagStr.includes("cli"))
    return { codeExt: ".sh", configExt: ".yaml" };
  if (tagStr.includes("java")) return { codeExt: ".java", configExt: ".xml" };
  if (tagStr.includes("c++") || tagStr.includes("c#"))
    return { codeExt: ".cpp", configExt: ".json" };
  if (tagStr.includes("docker") || tagStr.includes("container"))
    return { codeExt: ".sh", configExt: ".yml" };
  if (tagStr.includes("sql") || tagStr.includes("database"))
    return { codeExt: ".sql", configExt: ".json" };
  if (tagStr.includes("api") || tagStr.includes("rest") || tagStr.includes("http"))
    return { codeExt: ".js", configExt: ".json" };
  if (tagStr.includes("ml") || tagStr.includes("machine learning") || tagStr.includes("ai"))
    return { codeExt: ".py", configExt: ".yml" };
  if (tagStr.includes("data") || tagStr.includes("analytics") || tagStr.includes("visualization"))
    return { codeExt: ".py", configExt: ".json" };

  return { codeExt: ".py", configExt: ".yml" };
}

function generateMetadataYaml(skill: GeneratedSkill & { slug: string }): string {
  return `name: "${skill.name}"
category: ${skill.category}
description: "${skill.description.replace(/"/g, '\\"')}"
tags: [${skill.tags.map((t) => `"${t}"`).join(", ")}]
whenToUse: "${skill.whenToUse.replace(/"/g, '\\"')}"
source: generated
url: ""
generatedFrom: [${skill.generatedFrom.map((s) => `"${s}"`).join(", ")}]
prerequisites: [${(skill.prerequisites || []).map((s) => `"${s}"`).join(", ")}]
difficulty: ${skill.difficulty || "intermediate"}
estimatedTime: ${skill.estimatedTime || "1 hour"}
version: 1.0.0
createdAt: "${new Date().toISOString().split("T")[0]}"
`;
}

function generateReadme(skill: GeneratedSkill): string {
  const tutorialLines = skill.tutorial.split("\n");
  const steps = tutorialLines
    .filter((l) => l.startsWith("## ") || l.startsWith("### "))
    .slice(0, 5)
    .map((l) => l.replace(/^#{1,3}\s*/, ""))
    .join("\n- ");

  return `# ${skill.name}

${skill.description}

## 🎯 When to Use

${skill.whenToUse}

## 📋 Prerequisites

${(skill.prerequisites || []).map((p) => `- ${p}`).join("\n") || "No prerequisites required."}

## 📚 What You Will Learn

${(skill.whatYouWillLearn || []).map((w) => `- ${w}`).join("\n")}

## 📖 Quick Overview

${steps ? `- ${steps}` : "See TUTORIAL.md for the full guide."}

${skill.tutorial ? `## 🚀 Quick Tutorial\n\n${skill.tutorial.slice(0, 800)}${skill.tutorial.length > 800 ? "\n\n_...see TUTORIAL.md for the full tutorial_." : ""}\n\n` : ""}## 📁 File Structure

\`\`\`
${slugify(skill.name)}/
├── SKILL.md              ← Core skill definition
├── TUTORIAL.md           ← Step-by-step tutorial
├── README.md             ← This file
└── standards/
    ├── metadata.yml      ← Structured metadata
    ├── examples/         ← Example files
    └── templates/        ← Reusable templates
\`\`\`

## 🔗 Related Skills

This skill was generated by AI based on: ${skill.generatedFrom.join(", ")}.

---
*Generated by [SkillsSense](https://skillssense.vercel.app) · Difficulty: ${skill.difficulty || "intermediate"} · Est. time: ${skill.estimatedTime || "1 hour"}*
`;
}

