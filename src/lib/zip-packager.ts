/**
 * Package a generated skill into a ZIP buffer
 */
export async function packageSkill(skill: GeneratedSkill): Promise<PackagedSkill> {
  const slug = slugify(skill.name);
  const { codeExt, configExt } = getFileExtensions(skill.name, skill.tags);

  const zip = new JSZip();

  // 1. SKILL.md
  zip.file(
    "SKILL.md",
    `---\nname: ${skill.name}\ncategory: ${skill.category}\ndescription: ${skill.description}\ntags: [${skill.tags.join(", ")}]\nwhenToUse: ${skill.whenToUse}\nsource: generated\nurl: ""\ngeneratedFrom: [${skill.generatedFrom.map((s) => `"${s}"`).join(", ")}]\nprerequisites: [${(skill.prerequisites || []).map((s) => `"${s}"`).join(", ")}]\ndifficulty: ${skill.difficulty || "intermediate"}\nestimatedTime: ${skill.estimatedTime || "1 hour"}\n---\n\n# ${skill.name}\n\n${skill.description}\n\n## 🎯 When to Use\n\n${skill.whenToUse}\n\n${(skill.prerequisites || []).length > 0 ? `## 📋 Prerequisites\n\n${(skill.prerequisites || []).map((p) => `- ${p}`).join("\n")}\n\n` : ""}${skill.tutorial ? `## 📖 Tutorial\n\n${skill.tutorial}\n\n` : ""}## 📚 What You Will Learn\n\n${(skill.whatYouWillLearn || []).map((w) => `- ${w}`).join("\n")}\n\n## 🔗 Related Skills\n\nThis skill was generated by AI based on: ${skill.generatedFrom.join(", ")}.\n\n---\n*Generated by [SkillsSense](https://skillssense.vercel.app) · Difficulty: ${skill.difficulty || "intermediate"} · Est. time: ${skill.estimatedTime || "1 hour"}*\n`
  );

  // 2. TUTORIAL.md
  zip.file("TUTORIAL.md", skill.tutorial);

  // 3. README.md
  zip.file("README.md", generateReadme(skill));

  // 4. standards/
  const standardsDir = zip.folder("standards")!;

  // metadata.yml
  standardsDir.file(
    "metadata.yml",
    generateMetadataYaml({ ...skill, slug })
  );

  // examples/
  const examplesDir = standardsDir.folder("examples")!;
  for (const example of generateExamples(skill, codeExt)) {
    examplesDir.file(example.filename, example.content);
  }

  // templates/
  const templatesDir = standardsDir.folder("templates")!;
  for (const tmpl of generateTemplates(skill, codeExt, configExt)) {
    templatesDir.file(tmpl.filename, tmpl.content);
  }

  const buffer = await zip.generateAsync({ type: "nodebuffer" });

  return {
    zip: buffer,
    slug,
    files: [
      "SKILL.md",
      "TUTORIAL.md",
      "README.md",
      "standards/metadata.yml",
      ...generateExamples(skill, codeExt).map((e) => `standards/examples/${e.filename}`),
      ...generateTemplates(skill, codeExt, configExt).map((t) => `standards/templates/${t.filename}`),
    ],
  };
}
